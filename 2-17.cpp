/* Prints the numbers 1-4 in numerous ways, with adjacent 
 * numbers separated by a single space.
 */

#include <iostream>
using namespace std;

int main(){
    cout << "1 2 3 4\n";
    cout << "1 " << "2 " << "3 " << "4\n";
    cout << "1 ";
    cout << "2 ";
    cout << "3 ";
    cout << "4\n";
    
    return 0;
}
